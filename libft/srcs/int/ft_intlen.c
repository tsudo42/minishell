#include "ft_int.h"

/* ************************************************************************** */
/*  This function returns the minimum length to represent int n in C string,  */
/*  including negative sign.                                                  */
/*  e.g.) "3" -> 1, "0" -> 1, "1024" -> 4,  "-42" -> 2, "-2147483648" -> 11.  */
/* ************************************************************************** */
unsigned int	ft_intlen(int n)
{
	int	len;

	len = 0;
	if (n < 0)
		len++;
	n /= 10;
	len++;
	while (n != 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
